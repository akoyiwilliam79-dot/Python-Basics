#Write a program to calculate the area of a trapezium.
# area = 1/2(a+b)*h

a = float(input("Enter the value of the upper parallel side: "))
b = float(input("Enter the value of the lower parallel side: "))
h = float(input("Enter the height of the trapezium: "))

#area
area = 0.5 * (a + b) * h

print("The area of the trapezium is: {: .2f}" .format(area))


























