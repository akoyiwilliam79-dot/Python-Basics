#Arithmetics Operators
x = 10
y = 5
print(x + y)
print(x - y)
print(x * y) #multiplication
print(x / y) #division
print(x % y) #modulas - it displays the remainder after dividing

#comparison operators - they return a boolean
print(x < y)
print(x <= y)
print(x >= y)
print(x==y)# equal to
print(x != y)#not equal to

#assignment operators
a = 56
print(a)

#operator precedence - order in which operations get executed
a += 2
print(a)

#Logical operators - and, or, not - used when  more than settingone conditions at a time
print(100 < 200 and 100 == 200)
print(100 < 200 or 100 == 200)
print(not(100 < 200 or 100 == 200))

